package proje1;

public class date {
	private int day;
	private int mounth;
	private int year;
	private int hour;
	private int munite;
	public date(int day, int mounth, int year, int hour, int munite) {
		this.day = day;
		this.mounth = mounth;
		this.year = year;
		this.hour = hour;
		this.munite = munite;
		
		
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getMounth() {
		return mounth;
	}
	public void setMounth(int mounth) {
		this.mounth = mounth;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public int getMunite() {
		return munite;
	}
	public void setMunite(int munite) {
		this.munite = munite;
	}
	
	
	

}
